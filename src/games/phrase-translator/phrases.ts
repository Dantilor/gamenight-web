function shuffle<T>(items: T[]): T[] {
  const arr = [...items]
  for (let i = arr.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[arr[i], arr[j]] = [arr[j], arr[i]]
  }
  return arr
}

export function preparePhrasesForGame(count: RoundCountOption): PhraseCard[] {
  return shuffle(PHRASE_CARDS).slice(0, count)
}

export function pickAutoAnswer(): string {
  return AUTO_ANSWERS[Math.floor(Math.random() * AUTO_ANSWERS.length)]
}
