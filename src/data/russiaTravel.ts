export function getRussiaTravelDeckById(deckId: string): RussiaTravelDeck | undefined {
  return RUSSIA_TRAVEL_DECKS.find((deck) => deck.id === deckId)
}

export function getRussiaTravelCardsByDeck(deckId: string): RussiaTravelCard[] {
  return RUSSIA_TRAVEL_CARDS.filter((card) => card.deckId === deckId)
}

export function getRussiaTravelCardCount(deckId: string): number {
  return getRussiaTravelCardsByDeck(deckId).length
}

export function isRussiaTravelDeckLocked(deck: RussiaTravelDeck, isPremium: boolean): boolean {
  return deck.isPremium && !isPremium
}
